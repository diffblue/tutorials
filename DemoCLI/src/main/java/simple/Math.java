package simple;

public class Math {

    int sum(int a, int b) {
        return a + b;
    }

    protected long subtract(long a, long b) {
        return a - b;
    }

    public double multiply(float a, float b) {
        return a * b;
    }

    public double divide(double a, double b) {
        return a / b;
    }
}